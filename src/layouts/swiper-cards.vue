function startAnimation() {
  let animationInterval = null;
  let scrollDirection = 1; // 1 for scrolling right, -1 for scrolling left

  const startMoving = () => {
    cards.value.scrollLeft += 3 * scrollDirection;
    
    // Check if reached the end of the cards
    if (scrollDirection === 1 && cards.value.scrollLeft >= cards.value.scrollWidth - cards.value.offsetWidth) {
      // Change scroll direction to left
      scrollDirection = -8;
    } else if (scrollDirection === -1 && cards.value.scrollLeft <= 0) {
      // Change scroll direction to right
      scrollDirection = 1;
    }
  };

  const stopMoving = () => {
    clearInterval(animationInterval);
    setTimeout(() => {
      startAnimation();
    }, 3000);
  };

  animationInterval = setInterval(startMoving, 10); // Adjust interval as needed for smoother animation
  setTimeout(stopMoving, 300);
}
